#include "libmlxutil.h"

t_3dpoint	*mlx_eqadd_point(t_3dpoint *p1, t_3dpoint *p2)
{
	p1->i += p2->i;
	p1->j += p2->j;
	p1->k += p2->k;
	return (p1);
}
